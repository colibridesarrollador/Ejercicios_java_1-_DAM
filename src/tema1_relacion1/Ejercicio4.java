package tema1_relacion1;

/**
 * 
 * 4. Escribe un programa que declare 4 variables de tipo char. Asígnale una
 * letra a cada una. Muestra por pantalla: Primera letra:…. Segunda letra:…..
 * Tercera letra:…. Cuarta letra:…… Todas las letras juntas:……..
 * 
 * @author Colibrí
 *
 */
public class Ejercicio4 {

	public static void main(String[] args) {
		
		char primera = 'h';
		char segunda = 'o';
		char tercera = 'l';
		char cuarta = 'a';
		
		System.out.println("Primera letra: "+primera);
		System.out.println("Segunda letra: "+segunda);
		System.out.println("Tercera letra: "+tercera);
		System.out.println("Cuarta letra: "+cuarta);
		System.out.println("Todas las letras juntas: "+primera+segunda+tercera+cuarta);
	}

}
